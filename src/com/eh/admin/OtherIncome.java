    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        if (!incomeNameTextField.equals("")) {
            if (!incomeAmountTextField.equals("")) {
                String incomeName = incomeNameTextField.getText();
                String incomeAmount = incomeAmountTextField.getText();
                String details = detailsTextArea.getText();

                String tableName = " income_cost ";
                String columnName = " type, name, amount, details, input_date ";
                String values = "'OtherIncome', '" + incomeName + "', '" + incomeAmount + "', '" + details + "', now() ";
                try {
                    boolean AddIncomeCost = InsertQueryDao.insertQueryWithOutWhereClause(tableName, columnName, values);
                    if (!AddIncomeCost) {
                        JOptionPane.showMessageDialog(null, "Income Not Added");
                    } else {
                        JOptionPane.showMessageDialog(null, "Income Added");
                        incomeNameTextField.setText("");
                        incomeAmountTextField.setText("");
                        detailsTextArea.setText("");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(OtherIncome.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Give Amount");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Give Name");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

