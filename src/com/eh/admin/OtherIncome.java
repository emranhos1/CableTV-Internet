    private void incomeAmountTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_incomeAmountTextFieldKeyTyped
        char ch = evt.getKeyChar();
        if (!isNumber(ch) && ch != '\b') {
            evt.consume();
        }
    }//GEN-LAST:event_incomeAmountTextFieldKeyTyped
    private boolean isNumber(char ch) {
        return ch >= '0' && ch <= '9';
    }

